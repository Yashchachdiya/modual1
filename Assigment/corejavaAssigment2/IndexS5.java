/*W.A.J.P to get the character at the given index within the String. Original String =
Tops Technologies! The character at position 0 is T, The character at position 10 is
o
*/

package com.corejavaAssigment2;

public class IndexS5 
{
    public static void main(String[] args) 
    {
        String s = "Tops Technologies!";
     
        char c1 = s.charAt(0);
        System.out.println("The character at position 0 is " + c1);

        char c10 = s.charAt(10);
        System.out.println("The character at position 10 is " + c10);
    }
}
